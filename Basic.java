public class Basic {
    public static void main(String[] args) {
        int i = 10;
        if(i == 10)              // if will only execute for true condition
        {
            System.out.println("hello world");
        }


        int count = 0;
        while (count != 5){
            System.out.println(count);
            count++;
        }

        for (int cout =0; cout<3; count++){
            System.out.println(cout);
        }
    }
}
